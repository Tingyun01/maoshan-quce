// ============ 变体引擎 ============

/**
 * 从变体池中随机选取某个字段的变体
 * @param {object} variantPool - 某个类型的变体对象
 * @param {string} field - 字段名（title/sum/brief等）
 * @param {string} fallback - 如果变体池没有匹配，使用这个默认值
 * @returns {string} 随机选取的变体
 */
function pickVariant(variantPool, field, fallback) {
  const pool = variantPool && variantPool[field];
  if (!pool || !Array.isArray(pool) || pool.length === 0) return fallback || '';
  return pool[Math.floor(Math.random() * pool.length)];
}

/**
 * 对某次测试结果应用变体（保持原结果结构不变，随机替换有变体的字段）
 * @param {object} originalResult - 原始结果对象（来自 test config 的 results[type]）
 * @param {string} testId - 测试ID
 * @param {string} typeCode - 结果类型码
 * @returns {object} 应用变体后的结果对象
 */
function applyVariants(originalResult, testId, typeCode) {
  if (!originalResult) return originalResult;

  const rootPool = VARIANTS[testId];
  if (!rootPool) return originalResult; // 该测试没有变体配置，返回原值

  const variantPool = rootPool[typeCode];
  if (!variantPool) return originalResult; // 该类型没有变体配置，返回原值

  const result = { ...originalResult };

  // 对每个有变体配置的字段进行随机替换
  const fields = ['title', 'sum', 'brief', 'tagline', 'rank', 'othersView', 'trueSelf', 'strengths', 'weaknesses'];
  fields.forEach(field => {
    const variant = pickVariant(variantPool, field, result[field]);
    if (variant) result[field] = variant;
  });

  // 如果变体更新了 title，同时更新 sum 字段（兼容 quiz.js 存储逻辑）
  // sum 和 summary 是同义词
  if (result.sum && !result.summary) result.summary = result.sum;
  if (result.summary && !result.sum) result.sum = result.summary;

  return result;
}

/**
 * 获取某个测试/类型所有可能的变体数量（用于展示"无限"感）
 */
function getPossibilityCount(testId, typeCode) {
  const rootPool = VARIANTS[testId];
  if (!rootPool) return 1;
  const variantPool = rootPool[typeCode];
  if (!variantPool) return 1;

  const fields = ['title', 'sum', 'brief', 'tagline', 'rank', 'othersView', 'trueSelf', 'strengths', 'weaknesses'];
  let total = 1;
  fields.forEach(f => {
    const arr = variantPool[f];
    if (arr && Array.isArray(arr) && arr.length > 0) {
      total *= arr.length;
    }
  });
  return total; // 例如 5×4×5×5×4×3×3×4×4 = 约 57 万种组合
}

